const pantoneFunctions = {};

for (const color in pantone) {
    // Extract the color code
    const colorCode = pantone[color];

    // Function for text color
    pantoneFunctions[color] = (text) => chalk.hex(colorCode)(text);

    // Generate the background color function name
    const bgFunctionName = `bg${color.charAt(0).toUpperCase() + color.slice(1)}`;

    // Function for background color
    pantoneFunctions[bgFunctionName] = (text) => chalk.bgHex(colorCode)(text);
}

// Attach Pantone themes to Chalk
chalk.pantone = pantoneFunctions;

export default chalk;
